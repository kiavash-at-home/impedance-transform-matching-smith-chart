/* ----- */

void setheading (int angle)
{
  t_heading = angle % 360; /* avoid overflows */
}

/* ----- */

int heading (void)
{
  return (t_heading);
}

/* ----- */

void home (void)
{
  t_x = getmaxx () / 2;
  t_y = getmaxy () / 2;
  setheading (0);
}

/* ----- */

void forwd (int n)
{
#if __GNUC__
  short int newx, newy;
#else
  int newx, newy;
#endif
  /* why short int instead of int? I noticed a strange bug 
   * that occurs when compiling with gcc 4.6.3 on my Linux Mint 13.
   * Compiling with tcc or clang produces no errors. xvertext
   * memory leak in xbgi-ptoc, maybe?
   */
  
  newx = (int) ((double) n * t_cos[t_heading]);
  newy = (int) ((double) n * t_sin[t_heading]);
  
  /* is the turtle visible? */
  if (t_show_turtle && t_turtle_drawn)
    undraw_turtle ();
  
  /* should we draw? */
  if (t_pen_down)
    line (t_x, t_y, t_x + newx, t_y - newy);

  t_x += newx;
  t_y -= newy;
  
  if (t_show_turtle)
    draw_turtle ();
  
  /* is wrapping active? */
  if (t_turtle_wrap) {
    if (t_x < 0)
      t_x = getmaxx () - t_x;
    if (t_x > getmaxx ())
      t_x = t_x - getmaxx ();
    
    /* BUG HERE! using gcc, and defining t_y as integer, 
     * sometimes it overflows and turns < 0 */
    if (t_y < 0)
      t_y = getmaxy () - t_y;
    if (t_y > getmaxy ())
      t_y = t_y - getmaxy ();
  }
  
} /* forwd */

/* ----- */

void back (int n)
{
  turnleft (180);
  forwd (n);
  turnleft (180);
}

/* ----- */

void pendown (void)
{
  t_pen_down = T_TRUE;
}

/* ----- */

void penup (void)
{
  t_pen_down = T_FALSE;
}

/* ----- */

void turnleft (int n)
{
  t_heading -= n;
  if (t_heading < 0)
    t_heading += 360;
}

/* ----- */

void turnright (int n)
{
  t_heading += n;
  if (t_heading >= 360)
    t_heading -= 360;
}

/* ----- */

int xcor (void)
{
  return (t_x);
}

/* ----- */

int ycor (void)
{
  return (t_y);
}

/* ----- */

void draw_turtle (void)
{
  setwritemode(XOR_PUT);
  
  /* maybe too simple but convenient :-) */
  circle (t_x, t_y, 10);
  
  setwritemode(COPY_PUT);
  t_turtle_drawn = T_TRUE;
}

/* ----- */

void undraw_turtle (void)
{
  draw_turtle ();
  t_turtle_drawn = T_FALSE;
}

/* ----- */

void showturtle (void)
{
  draw_turtle ();
  t_show_turtle = T_TRUE;
}

/* ----- */

void hideturtle (void)
{
  draw_turtle ();
  t_show_turtle = T_FALSE;
}

/* ----- */

void setposition (int x, int y)
{
  t_x = x;
  t_y = y;
}

/* ----- */

void wrap (void)
{
  t_turtle_wrap = T_TRUE;
}

/* ----- */

void nowrap (void)
{
  t_turtle_wrap = T_FALSE;
}

